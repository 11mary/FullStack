public class datatype {
    public static void main(String[] args) {
        boolean a=true;
        System.out.println(a);
        char b='A';
        System.out.println(b);
        int c=20;
        System.out.println(c);
        float d=3.14f;
        System.out.println(d);
        double e=22233d;
        System.out.println(e);

    }
}
